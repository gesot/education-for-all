            <div class="embed-responsive embed-responsive-21by9">
                <!-- 16:9 aspect ratio -->
                <iframe width="560" height="315" src="https://www.youtube.com/embed/oCEB-uH49AQ" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
            </div>
            <p class="description">Please Vote for Me takes us inside an elementary school classroom in Wuhan, China, where students are preparing for arguably the biggest event of the year: electing their class official. Remember, the democratic process is something of a rarity in Communist China, so watching these fledging pols figure it out as they go is half the fun. The real meat of the film, though, is in its examination of what success in the classroom means for these kids as well as their parents (many of whom are every bit as invested in the outcome of the election as the candidates themselves).
