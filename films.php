                <iframe width="560" height="315" src="https://www.youtube.com/embed/I5hE2Xm_dDQ" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
            </div>
            <p class="description">
                Part documentary and part social-justice campaign, Teach Us All revisits the violent resistance to the desegregation of Central High
                in Little Rock, Arkansas in 1957. Sixty years after a group of African American students (known as the Little Rock Nine) defied racism
                and hatred to attend an all-white school, Teach Us All takes us back to the city where it all began.
                The modern-day Little Rock school district once again faces upheaval as it navigates a recent state takeover and wrestles with
                civil rights issues facing schools across the nation. Educational inequality is at the forefront of this compelling documentary,
                leaving viewers with the lesson that some history is less settled than many would like to believe.

            </p>
        </div>

